public class Program4 {
    public static void main(String[] args) {

        // Initialize the characters
        char ch1 = 'b';
        char ch2 = 'a';

        // Determine order and print
        if (ch1 < ch2) {
            System.out.println(ch1 + ", " + ch2);
        } else {
            System.out.println(ch2 + ", " + ch1);
        }
    }
}
